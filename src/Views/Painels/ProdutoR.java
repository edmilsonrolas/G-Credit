     public void combo(){
         List cat = gero.listarOperadoras();

        Iterator it = cat.iterator();
        
        while (it.hasNext()) {
            
           Operadora produto = (Operadora) it.next();
            String nome = produto.getNome();

            model.addElement(nome);
        }
        
        combox.setModel(model);
    }
    
    public void tabela(){
        modelo.setRowCount(0);
        
        List produtos = gestor.listarProdutos();
        
        Iterator it = produtos.iterator();
        
        while(it.hasNext()){
            Produto produto = (Produto) it.next();
            Object[] fila = new Object[8];
            fila[0] = produto.getId();
            fila[1] = produto.getNomeProduto();
            fila[2] = produto.getPreco();
            fila[3] = produto.getValor();
            fila[4] = produto.getQuantidadeProduto();
            fila[5] = produto.getFornecedor();
            fila[6] = produto.getValidadeProduto();
            fila[7] = produto.getEntrada();
            
            modelo.addRow(fila);
        }
        
        tabela.setModel(modelo);
       
    }

