    private void jPanel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel6MouseClicked
       String nome = nomeField.getText();
       double preco = Double.parseDouble(prcField.getText());
       double valor = Double.parseDouble(vlrField.getText());
       int quantidade = Integer.parseInt(qtdField.getText());
       
       int selectedIndex = combox.getSelectedIndex();
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um Fornecedor.");
            return;
        }
        
        List cat = gero.listarOperadoras();
        
        if (cat != null && selectedIndex >= 0 && selectedIndex < cat.size()) {
            Operadora prod = (Operadora) cat.get(selectedIndex);
            Produto cliente = new Produto(nome,new Date(),preco,valor,quantidade,prod);

            try {
                gestor.adicionarProduto(cliente);
                JOptionPane.showMessageDialog(null, "novo Produto adicionado!");
                nomeField.setText("");
                prcField.setText("");
                vlrField.setText("");
                qtdField.setText("");
                tabela();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Fornecedor selecionada inválida.");
        }
    }//GEN-LAST:event_jPanel6MouseClicked

    private void jPanel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel5MouseClicked
        List<Operadora> cs = gero.listarOperadoras();
        List<Produto> clientes = gestor.listarProdutos();
        int com = combox.getSelectedIndex();
        int selected = tabela.getSelectedRow();

        if (selected != -1) {
            Operadora cat = cs.get(com);
            String nome = nomeField.getText();
            double preco = Double.parseDouble( prcField.getText());
            double valor = Double.parseDouble(vlrField.getText());
            
            int qtd = Integer.parseInt(qtdField.getText());

            //            String nome = JOptionPane.showInputDialog("Editar nome do cliente:", modelo.getValueAt(selected, 1));
            //            String email = JOptionPane.showInputDialog("Editar email do cliente:", modelo.getValueAt(selected, 1));
            if (nome != null && !nome.isEmpty()) {
                int id = (int) modelo.getValueAt(selected, 0);
                Produto cliente = gestor.buscarProduto(id);
                        cliente.setNomeProduto(nome);
                        cliente.setPreco(preco);
                        cliente.setValor(valor);
                        cliente.setQuantidadeProduto(qtd);
                        
                        gestor.atualizarProdutos(cliente);
                        
                        JOptionPane.showMessageDialog(null, "Produto Atualizado!");
                tabela();

            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Produto para editar.");
        }
    }//GEN-LAST:event_jPanel5MouseClicked


