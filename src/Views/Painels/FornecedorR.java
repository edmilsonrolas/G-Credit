    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
       String nome = nomeField.getText();
        String contato = contField.getText();
        String desc = descField.getText();

        Operadora forn = new Operadora(nome, contato, desc);

        try {
            gestor.adicionarOperadora(forn);
            JOptionPane.showMessageDialog(null, "Operadora Registrada!");
            nomeField.setText("");
            contField.setText("");
            descField.setText("");
            tabela();
        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);

        }
    }//GEN-LAST:event_addActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        List<Operadora> forns = gestor.listarOperadoras();
        int selected = tabela.getSelectedRow();

        if (selected != -1) {
//
        String nome = nomeField.getText();
        String desc = descField.getText();
        String cont = contField.getText();
//
//            //            String nome = JOptionPane.showInputDialog("Editar nome do cliente:", modelo.getValueAt(selected, 1));
//            //            String email = JOptionPane.showInputDialog("Editar email do cliente:", modelo.getValueAt(selected, 1));
            if (nome != null && !nome.isEmpty()) {
                int id = (int) modelo.getValueAt(selected, 0);
                for (Operadora forn : forns) {
                    if (forn.getId() == id) {
                        forn.setNome(nome);
                        forn.setContacto(cont);    
                        gestor.atualizarOperadora(forn);
                    }
                }
                tabela();

            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma Operadora para editar.");
        }
    }//GEN-LAST:event_editActionPerformed


