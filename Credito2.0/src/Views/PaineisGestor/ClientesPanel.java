    private void addBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBtnMouseClicked
        String nome = nomeField.getText();
        String email = emailField.getText();
        String senha = senhaField.getText();
        int local = nivelBox.getSelectedIndex();
        String[] nivel = {"Admin", "Cliente", "Vendor"};
       
        
                Clientes cliente = new Clientes();
                
                cliente.setNome(nome);
                cliente.setEmail(email);
                cliente.setSenha(senha);
        if (local != -1) {
            try {
                

                if (local == 0) {
                    cliente.setNivelPerm(nivel[0]);
                } else if (local == 1) {
                    cliente.setNivelPerm(nivel[1]);
                } else {
                    cliente.setNivelPerm(nivel[2]);
                }
                cliente.setAdmissao(new Date());

                gerente.registarCliente(cliente);
                JOptionPane.showMessageDialog(null, "registro efectuado");
                relatorio("Registrou um cliente.");
                nomeField.setText("");
                emailField.setText("");

                iniciarTabela();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_addBtnMouseClicked

    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
        int selected = tabela.getSelectedRow();

        if (selected != -1) {
            
            String nome = nomeField.getText();
            String email = emailField.getText();
            String senha = senhaField.getText();
            int local = nivelBox.getSelectedIndex();
            String[] nivel = {"Admin", "Cliente", "Vendor"};

//            String nome = JOptionPane.showInputDialog("Editar nome do cliente:", modelo.getValueAt(selected, 1));
//            String email = JOptionPane.showInputDialog("Editar email do cliente:", modelo.getValueAt(selected, 1));
            if (nome != null && !nome.isEmpty()) {
                int id = (int) modelo.getValueAt(selected, 0);
                for (Clientes cliente : gerente.listarClientes()) {
                    if (cliente.getClienteId() == id) {
                        cliente.setNome(nome);
                        cliente.setEmail(email);
                        cliente.setSenha(senha);
                  
                        if (local == 0) {
                            cliente.setNivelPerm(nivel[0]);
                        } else if (local == 1) {
                            cliente.setNivelPerm(nivel[1]);
                        } else {
                            cliente.setNivelPerm(nivel[2]);
                        }
                        cliente.setAdmissao(new Date());
                        
                        gerente.actualizarCliente(cliente);
                        relatorio("Editou Dados sobre o cliente.");
                    }
                }
                iniciarTabela();

            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente para editar.");
        }

    }//GEN-LAST:event_editBtnMouseClicked

    private void jPanel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel7MouseClicked
        int selected = tabela.getSelectedRow();
        if (selected != -1) {
            int id = (int) modelo.getValueAt(selected, 0);
            for (Clientes cli1 : cli) {
                if (cli1.getClienteId() == id) {
                    cli1.setStatus(true);
                    gerente.actualizarCliente(cli1);
                    JOptionPane.showMessageDialog(null, cli1.getNome() + " excluido da lista de clientes.");
                    relatorio("Excluiu um cliente.");
                }
            }

            iniciarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente para excluir.");
        }
    }//GEN-LAST:event_jPanel7MouseClicked

