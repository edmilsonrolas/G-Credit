    private void iniciarTabela() {

        modelo.setRowCount(0);

        List clientes = gerente.listarClientes();
        Iterator it = clientes.iterator();
        while (it.hasNext()) {
            Clientes client = (Clientes) it.next();
            if(client.getNivelPerm().equals("Cliente")){
            Object[] fila = new Object[5];
            fila[0] = client.getClienteId();
            fila[1] = client.getNome();
            fila[2] = client.getEmail();
            fila[3] = client.getNivelPerm();
            fila[4] = client.getAdmissao();

            modelo.addRow(fila);
            }
        }
        tabela.setModel(modelo);
        revalidate();
        repaint();
    }

