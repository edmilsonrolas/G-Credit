    private void iniciarTabela(){
        modelo.setRowCount(0);
        
        List clientes = gerente.listarProdutos();
         Iterator it = clientes.iterator();
         while(it.hasNext()) {
             Produtos prdt = (Produtos) it.next();
             Object[] fila = new Object[3];
             fila[0] = prdt.getProdutoId();
             fila[1] = prdt.getNome();
             fila[2] = prdt.getPreco();
             
             modelo.addRow(fila);
         }
         tabela.setModel(modelo);
    }
