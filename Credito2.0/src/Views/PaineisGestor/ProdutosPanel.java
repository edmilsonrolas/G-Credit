    private void addBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBtnMouseClicked
        String nome = nomeField.getText();
        long preco = Long.parseLong(emailField.getText());

        produto = new Produtos();
        produto.setProdutoId(produtos.size()+1);
        produto.setNome(nome);
        produto.setPreco(preco);

        try{
            gerente.registarProduto(produto);
            JOptionPane.showMessageDialog(null, "registro efectuado");
            iniciarTabela();
            relatorio("Registrou um Produto.");
            nomeField.setText("");
            emailField.setText("");

            

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro de cadastro");
        }

    }//GEN-LAST:event_addBtnMouseClicked

    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
        int selected = tabela.getSelectedRow();

        if (selected != -1) {

            String nome = nomeField.getText();
            String email = emailField.getText();

            //            String nome = JOptionPane.showInputDialog("Editar nome do cliente:", modelo.getValueAt(selected, 1));
            //            String email = JOptionPane.showInputDialog("Editar email do cliente:", modelo.getValueAt(selected, 1));
            if (nome != null && !nome.isEmpty()) {
                int id = (int) modelo.getValueAt(selected, 0);
                for (Produtos produto1 : gerente.listarProdutos()) {
                    if (produto1.getProdutoId() == id) {
                        produto1.setNome(nome);
                        produto1.getPreco();
                        gerente.actualizarProduto(produto1);
                        relatorio("Editou Dados sobre o Produto.");
                    }
                }
                iniciarTabela();

            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Produto para editar.");
        }

    }//GEN-LAST:event_editBtnMouseClicked

    private void jPanel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel8MouseClicked
        int selected = tabela.getSelectedRow();
        if (selected != -1) {
            int id = (int) modelo.getValueAt(selected, 0);
            for (Produtos produto1 : produtos) {
                if(produto1.getProdutoId()==id){
                    gerente.removerProduto(produto1);
                    JOptionPane.showMessageDialog(null, produto1.getNome() +" excluido da lista de clientes.");
                    relatorio("Removeu um Produto.");
                }
            }

            iniciarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Produto para excluir.");
        }
    }//GEN-LAST:event_jPanel8MouseClicked

