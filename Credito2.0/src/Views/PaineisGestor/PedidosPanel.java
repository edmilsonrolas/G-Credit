    private void comboBox() {
        List produtos = gerente.listarProdutos();

        Iterator it = produtos.iterator();
        while (it.hasNext()) {
            Produtos produto = (Produtos) it.next();
            String nome = produto.getNome();

            model.addElement(nome + " -- " + produto.getPreco() + "MZN");
        }
        jComboBox1.setModel(model);
    }
