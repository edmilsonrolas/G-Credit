    private void addBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBtnMouseClicked

        int selectedIndex = jComboBox1.getSelectedIndex();
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um produto.");
            return;
        }

        String emailText = emailField.getText();
        int quantidade;
        try {
            quantidade = Integer.parseInt(emailText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Quantidade inválida. Insira um número inteiro.");
            return;
        }

        if (produtos != null && selectedIndex >= 0 && selectedIndex < produtos.size()) {
            Produtos prod = produtos.get(selectedIndex);

            try {
                gerente.fazerPedido(clien, prod, quantidade, null);
                JOptionPane.showMessageDialog(null, "Pedido efetuado com sucesso.");
                emailField.setText("");
                iniciarTabela();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao criar o pedido.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Produto selecionado inválido.");
        }


    }//GEN-LAST:event_addBtnMouseClicked

